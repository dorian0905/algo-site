// start javascript
function fonction_javascript() {
    const array1 = [4, 8, 7, 9, 1, 5, 4, 6];
    const array2 = [7, 6, 5, 2, 1, 3, 7, 4];
    let array3 = [];
    for(i = 0; i < array1.length; i++) {
        array3[i] = array1[i] + array2[i];
    }
    return array3;
}

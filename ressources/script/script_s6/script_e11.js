// start javascript
function fonction_javascript() {
    const array1 = [4, 8, 7, 12];
    const array2 = [3, 6];
    let result = 0;
    for(let i = 0; i < array1.length; i++) {
       for(j = 0; j < array2.length; j++) {
            result += array1[i] * array2[j];
       }
    }
    return result;
}
// end javascript


